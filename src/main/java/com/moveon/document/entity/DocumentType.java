package com.moveon.document.entity;

/**
 * 文档类型枚举
 */
public enum DocumentType {
    /**
     * 纯文本文件
     */
    TXT,

    /**
     * PDF 文档
     */
    PDF,

    /**
     * Word 文档
     */
    DOCX,

    /**
     * Excel 表格
     */
    XLSX,

    /**
     * PowerPoint 演示文稿
     */
    PPTX,

    /**
     * 图片文件
     */
    IMAGE,

    /**
     * 其他类型
     */
    OTHER
}
